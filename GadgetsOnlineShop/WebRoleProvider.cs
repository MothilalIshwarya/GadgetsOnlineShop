        public override string[] GetRolesForUser(string username)
        {
            using (var context = new GadgetDetailEntities2())
            {
                //var result = from user in context.UserDetails
                var result = (from user in context.UserDetails
                              join role in context.Roles on user.UserName equals role.RUserName
                              where user.UserName == username
                              select role.Role1).ToArray();
                return result;
